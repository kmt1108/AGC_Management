    private void initTable(){
        tbCustomer.removeAll();
        DefaultTableModel model= new DefaultTableModel(new Object [][]{},new String [] {"Customer ID", "Customer Name", "Company Name", "Company Address", "SDT"});
        for (Customer cus : currentlistCustomer) {
            model.addRow(new Object[]{String.valueOf(cus.getId()),cus.getName(),cus.getCompanyName(),cus.getCompanyAddress(),cus.getContactPhone()});
        }
        tbCustomer.setModel(model);
    }
    private void initConnection(){
        Connection conn=new DBConnection().getConnection();
        manager=new ManagementProcess(conn);
    }
    private void fillData(Customer cus) throws SQLException{
        txtID.setText(String.valueOf(cus.getId()));
        txtName.setText(cus.getName());
        txtCPName.setText(cus.getCompanyName());
        txtCPAddr.setText(cus.getCompanyAddress());
        txtSdt.setText(cus.getContactPhone());
        txtEmail.setText(cus.getContactEmail());
        txtUser.setText(cus.getUsername());
        txtPass.setText(manager.getPass(cus.getUsername()));
    }
    private boolean checkUser(String user,Customer oldCus,String type) throws SQLException{
        if (type.equals("EDIT")) {
            if (user.equals(oldCus.getUsername())) {
                return true;
            } else {
                ArrayList<String> lstUser = manager.getAllUser();
                for (String str : lstUser) {
                    if (str.equals(user)) {
                        return false;
                    }
                }
                return true;
            }
        }else if (type.equals("ADD")) {
                ArrayList<String> lstUser = manager.getAllUser();
                for (String str : lstUser) {
                    if (str.equals(user)) {
                        return false;
                    }
                }
                return true;
        }else
            return false;
        
    }

