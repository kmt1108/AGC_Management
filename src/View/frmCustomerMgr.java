    private void btAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddActionPerformed
        try {
            // TODO add your handling code here:
            String user=txtUser.getText().trim();
            String pass=txtPass.getText().trim();
            if (checkUser(txtUser.getText().trim(),currentlistCustomer.get(tbCustomer.getSelectedRow()),"ADD")) {
                if (manager.insertAccount(new Account(user,pass,"CU"))) {
                    if (manager.insertCustomer(new Customer(Integer.parseInt(txtID.getText().trim()), txtName.getText().trim(), txtCPName.getText().trim(), txtCPAddr.getText(), txtSdt.getText().trim(), txtEmail.getText().trim(), txtUser.getText().trim()))) {
                        JOptionPane.showMessageDialog(this, "Add customer successful!");
                        new frmCustomerMgr().setVisible(true);
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "Can't add new customer!");
                    }
                } else {
                    if (manager.updateAccount(user, new Account(user, pass, "CU"))) {
                        if (manager.insertCustomer(new Customer(Integer.parseInt(txtID.getText().trim()), txtName.getText().trim(), txtCPName.getText().trim(), txtCPAddr.getText(), txtSdt.getText().trim(), txtEmail.getText().trim(), txtUser.getText().trim()))) {
                            JOptionPane.showMessageDialog(this, "Add customer successful!");
                            new frmCustomerMgr().setVisible(true);
                            this.dispose();
                        } else {
                            JOptionPane.showMessageDialog(this, "Can't add new customer!");
                        }
                    }else
                        JOptionPane.showMessageDialog(this, "Can't add new customer!");

                }
                    
            }else
                JOptionPane.showMessageDialog(this, "This username exist!");
        } catch (SQLException ex) {
            Logger.getLogger(frmCustomerMgr.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btAddActionPerformed

    private void btEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            Customer cus=currentlistCustomer.get(tbCustomer.getSelectedRow());
            if (checkUser(txtUser.getText().trim(),cus,"EDIT")) {
                if (manager.updateAccount(cus.getUsername(), new Account(txtUser.getText().trim(), txtPass.getText(), "CU"))) {
                    if (manager.updateCustomer(cus.getId(), new Customer(Integer.parseInt(txtID.getText().trim()), txtName.getText().trim(), txtCPName.getText().trim(), txtCPAddr.getText(), txtSdt.getText().trim(), txtEmail.getText().trim(), txtUser.getText().trim()))) {
                        JOptionPane.showMessageDialog(this, "Update customer successful!");
                        new frmCustomerMgr().setVisible(true);
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "Can't upadate info customer!\n User/password updated!");
                    }
                }else
                    JOptionPane.showMessageDialog(this, "Can't use this account!!");
                
            }else
                JOptionPane.showMessageDialog(this, "This username exist!");
        } catch (SQLException ex) {
            Logger.getLogger(frmCustomerMgr.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btEditActionPerformed

